                        <h1>Getting Started with LetReach</h1>
                        <p>This page will help you get started with LetReacg API's. You'll be up and running in no time.!.</p>
                       <hr/>
                       <p>LetReach is a powerful new way of communicating with users. It allows websites on desktop and mobile to send push notifications to users. The notifications appear even when the user is not on the website, so they make for a great way to inform users and make them take actions (by clicking on notifications).</p>

                        <p>This document will help you understand the basics of the LetReach API.</p>

                        <p>The JavaScript API allows you to control subscriptions of individual subscribers. It can be used to:</p>
                        <ul>
                        <li>Trigger opt-in boxes for subscribing to push notifications</li>
                        <li>Get current subscriber details (including unique ID)</li>
                        <li>Add subscribers to segments</li>
                        <li>Remove subscribers from segments</li>
                        </ul>
                        <p>The REST API can be used to:</p>
                          <ul>
                        <li>Send notifications to all subscribers/a list of subscribers/subscribers belonging to a particular segment, or even a particular subscriber</li>
                        <li>Add or remove subscribers from segments</li>
                        <li>Check reports of previous notification requests</li>
                        </ul>
